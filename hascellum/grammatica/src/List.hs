irun :: Text -> Text
irun x = tokenToText $ outerToken x

run :: Text -> Text
run x = syllablesToText . sylparToSyllables . textToSylparO x . textToTexts . tokenToText $ outerToken x

run3 :: [] Text -> [] (Text, Text, Text)
run3 list = DL.zip3 list (map irun list) (map run list)

